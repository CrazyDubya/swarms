def print_results(results: Dict[str, Any], title: str):
    """Print formatted results"""
    print(f"\n{'='*60}")
    print(f"ğŸ“Š {title}")
    print('='*60)
    
    for key, value in results.items():
        if key != 'error':
            print(f"\nğŸ”¸ {key.upper().replace('_', ' ')}:")
            print("-" * 40)
            if isinstance(value, str):
                print(value[:500] + "..." if len(value) > 500 else value)
            else:
                print(str(value)[:500] + "..." if len(str(value)) > 500 else str(value))
    
    if 'error' in results:
        print(f"\nâŒ ERROR: {results['error']}")


def view_output_files():
    """View generated research files"""
    output_dir = Path("research_output")
    
    if not output_dir.exists():
        print("\nğŸ“ No research output directory found. Run some analyses first!")
        return
    
    files = list(output_dir.glob("*.md")) + list(output_dir.glob("*.txt"))
    
    if not files:
        print("\nğŸ“ No research files found in output directory.")
        return
    
    print(f"\nğŸ“ Found {len(files)} research files:")
    
    for i, file in enumerate(files, 1):
        print(f"{i}. {file.name}")
    
    try:
        choice = input("\nEnter file number to view (or 0 to return): ")
        choice = int(choice)
        
        if choice == 0:
            return
        elif 1 <= choice <= len(files):
            file_path = files[choice - 1]
            print(f"\nğŸ“„ Contents of {file_path.name}:")
            print("=" * 60)
            
            with open(file_path, 'r', encoding='utf-8') as f:
                content = f.read()
                print(content)
            
            print("=" * 60)
        else:
            print("âŒ Invalid file number")
            
    except (ValueError, FileNotFoundError) as e:
        print(f"âŒ Error: {e}")


